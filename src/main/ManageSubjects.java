    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:ZZ
        // 1. Get the selected row index from the table
   int row = tblMasterlist.getSelectedRow();
    
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Palihog pili og subject sa table!");
        return;
    }

    // Kuhaon ang ID gikan sa Column 0 (s_id)
    String id = tblMasterlist.getValueAt(row, 0).toString();
    String subName = tblMasterlist.getValueAt(row, 2).toString();

    int confirm = JOptionPane.showConfirmDialog(this, "I-delete ang " + subName + "?", "Confirm", JOptionPane.YES_NO_OPTION);
    
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            Connection conn = DriverManager.getConnection("jdbc:sqlite:info.db");
            // Siguroha nga tbl_subjects ang gamit
            String sql = "DELETE FROM tbl_subjects WHERE s_id = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, id);
            
            int deleted = pstmt.executeUpdate();
            
            if (deleted > 0) {
                logAction("Deleted Subject: " + subName); // Mo-save sa tbl_logs
                JOptionPane.showMessageDialog(this, "Successfully Deleted!");
                
                loadTable(); // Kini ang mo-refresh sa screen
            }
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

