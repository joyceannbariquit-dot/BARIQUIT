    public void loadTable() {
    DefaultTableModel model = (DefaultTableModel) tblMasterlist.getModel();
    
    // I-set ang mga Columns (Headers)
    model.setColumnCount(0); 
    model.addColumn("ID");
    model.addColumn("Grade Level");
    model.addColumn("Subject Name");
    model.addColumn("Teacher Name");
    
    // I-clear ang karaan nga data
    model.setRowCount(0); 
    
    try {
        Connection conn = DriverManager.getConnection("jdbc:sqlite:info.db");
        String sql = "SELECT * FROM tbl_subjects";
        PreparedStatement pstmt = conn.prepareStatement(sql);
        ResultSet rs = pstmt.executeQuery();
        
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("s_id"), 
                rs.getString("grade_level"), 
                rs.getString("subject_name"), 
                rs.getString("teacher_name")
            });
        }
        conn.close();
    } catch (Exception e) {
        System.out.println("Error: " + e.getMessage());
    }
}

