    public void logAction(String action) {
   
       try {
        Connection conn = DriverManager.getConnection("jdbc:sqlite:info.db");
        String sql = "INSERT INTO tbl_logs (user_email, action) VALUES (?, ?)";
        PreparedStatement pstmt = conn.prepareStatement(sql);
        
        // Kung null ang email, ibutang nato "Admin_User"
        String email = (adminEmail == null || adminEmail.isEmpty()) ? "Admin_User" : adminEmail;
        
        pstmt.setString(1, email); 
        pstmt.setString(2, action);
        pstmt.executeUpdate();
        conn.close();
    } catch (Exception e) { 
        System.out.println("Log Error: " + e.getMessage()); 
    }
}

