    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
    String teacher = txtTeacher.getText().trim(); 
    String section = txtSection.getText().trim();
    String subject = txtSubject.getText().trim();
    String email = txtEmail.getText().trim();
    String gradeLevel = cmbGrade.getSelectedItem().toString();

    if(teacher.isEmpty() || section.isEmpty() || subject.isEmpty() || gradeLevel.equals("GRADE & LEVEL")){
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields!");
        return;
    }

    try {
        java.sql.Connection conn = config.config.connectDB();
        // GI-REMOVE ANG 'status' SA SQL QUERY SA UBOS:
        String sql = "INSERT INTO tbl_transactions (user_email, grade_level, teacher_name, section, subject) VALUES (?, ?, ?, ?, ?)";
        java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);
        
        pstmt.setString(1, email);
        pstmt.setString(2, gradeLevel);
        pstmt.setString(3, teacher);
        pstmt.setString(4, section);
        pstmt.setString(5, subject); // 5 na lang kabuok ang parameters (?)
        
        pstmt.executeUpdate();
        
        pstmt.close();
        conn.close(); 
        
        javax.swing.JOptionPane.showMessageDialog(this, "Saved Successfully!");
        
        displayTransactions(); // Refresh ang table
        
        // Limpyo sa fields
        txtTeacher.setText("");
        txtSection.setText("");
        txtSubject.setText("");
        
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }



    }//GEN-LAST:event_btnSubmitActionPerformed

