    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        // 1. Kuhaon ang index sa row nga gi-click
    int row = jTable1.getSelectedRow();
    
    // 2. Siguroha nga naay napili nga row (dili -1)
    if (row != -1) {
        // Base sa imong displayData() method, kini ang arrangement sa columns:
        // Index 0 = teacher_name, 1 = subject, 2 = section, 3 = grade_level, 4 = status
        
        String teacher = jTable1.getValueAt(row, 0).toString();
        String subject = jTable1.getValueAt(row, 1).toString();
        String section = jTable1.getValueAt(row, 2).toString();
        String gradeLvl = jTable1.getValueAt(row, 3).toString();
        
        
        // 3. I-set ang data ngadto sa imong mga text fields ug combobox
        txtTeacher.setText(teacher);
        txtSubject.setText(subject);
        txtSection.setText(section);
        cmbGrade.setSelectedItem(gradeLvl);
        
        // Siguroha nga visible ang email (txtEmail) kay gigamit man ni sa submission
        txtEmail.setText(userEmail); 
    }

        
    }//GEN-LAST:event_jTable1MouseClicked

