private void loadTable() {
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    // I-set ang columns base sa imong tbl_grades sa info.db
    model.setColumnIdentifiers(new Object[]{"ID", "Student", "Level", "Section", "Subject", "Q1", "Q2", "Q3", "Q4", "Average"});

    try (java.sql.Connection conn = java.sql.DriverManager.getConnection("jdbc:sqlite:info.db")) {
        java.sql.ResultSet rs = conn.createStatement().executeQuery("SELECT * FROM tbl_grades");
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("grade_id"),
                rs.getString("student_name"),
                rs.getString("grade_level"),
                rs.getString("section"),
                rs.getString("subject"),
                rs.getDouble("q1"),
                rs.getDouble("q2"),
                rs.getDouble("q3"),
                rs.getDouble("q4"),
                rs.getString("average")
            });
        }
    } catch (Exception e) {
        System.out.println("Error loading table: " + e.getMessage());
    }
}

