    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
    
    String sql = "INSERT INTO tbl_grades (student_name, grade_level, section, subject, q1, q2, q3, q4, average) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
    
    try (java.sql.Connection conn = java.sql.DriverManager.getConnection("jdbc:sqlite:info.db");
         java.sql.PreparedStatement pstmt = conn.prepareStatement(sql)) {
        
        // Siguroha nga ang txtName.getText() parehas sa Fullname sa tbl_user
        pstmt.setString(1, txtName.getText().trim()); 
        pstmt.setString(2, jComboBox1.getSelectedItem().toString());
        pstmt.setString(3, txtSection.getText());
        pstmt.setString(4, txtSubject.getText());
        pstmt.setDouble(5, Double.parseDouble(txtQ1.getText()));
        pstmt.setDouble(6, Double.parseDouble(txtQ2.getText()));
        pstmt.setDouble(7, Double.parseDouble(txtQ3.getText()));
        pstmt.setDouble(8, Double.parseDouble(txtQ4.getText()));
        
        // Calculate Average
        double avg = (Double.parseDouble(txtQ1.getText()) + Double.parseDouble(txtQ2.getText()) + 
                      Double.parseDouble(txtQ3.getText()) + Double.parseDouble(txtQ4.getText())) / 4;
        pstmt.setString(9, String.format("%.2f", avg));

        pstmt.executeUpdate();
        javax.swing.JOptionPane.showMessageDialog(null, "Grade Saved Successfully!");
        loadTable(); // I-refresh ang table sa admin
        
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
    int row = jTable1.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Palihog pili og row sa table.");
        return;
    }
    String id = jTable1.getValueAt(row, 0).toString(); // Kuhaon ang ID

    double g1 = Double.parseDouble(txtQ1.getText());
    double g2 = Double.parseDouble(txtQ2.getText());
    double g3 = Double.parseDouble(txtQ3.getText());
    double g4 = Double.parseDouble(txtQ4.getText());
    double avg = (g1 + g2 + g3 + g4) / 4.0;

    java.sql.Connection conn = java.sql.DriverManager.getConnection("jdbc:sqlite:info.db");
    String sql = "UPDATE tbl_grades SET student_name=?, grade_level=?, section=?, subject=?, q1=?, q2=?, q3=?, q4=?, average=? WHERE grade_id=?";
    java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);

    pstmt.setString(1, txtName.getText());
    pstmt.setString(2, jComboBox1.getSelectedItem().toString());
    pstmt.setString(3, txtSection.getText());
    pstmt.setString(4, txtSubject.getText());
    pstmt.setDouble(5, g1);
    pstmt.setDouble(6, g2);
    pstmt.setDouble(7, g3);
    pstmt.setDouble(8, g4);
    pstmt.setString(9, String.format("%.2f", avg));
    pstmt.setString(10, id);

    pstmt.executeUpdate();
    javax.swing.JOptionPane.showMessageDialog(this, "Record Updated!");
    loadTable(); // Refresh table
    conn.close();
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Update Error: " + e.getMessage());
}
    }//GEN-LAST:event_btnUpdateActionPerformed

